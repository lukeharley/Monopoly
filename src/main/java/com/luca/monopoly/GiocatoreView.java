package com.luca.monopoly;

public class GiocatoreView {

    private String nome;

    private int portafoglio;

    private String segnalino;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getPortafoglio() {
        return portafoglio;
    }

    public void setPortafoglio(int portafoglio) {
        this.portafoglio = portafoglio;
    }

    public String getSegnalino() {
        return segnalino;
    }

    public void setSegnalino(String segnalino) {
        this.segnalino = segnalino;
    }
}
